import UIKit

extension UIColor {
    open class var grey20: UIColor {
        get {
            return UIColor(named: "Grey20") ?? .white
        }
    }
    
    open class var grey40: UIColor {
        get {
            return UIColor(named: "Grey40") ?? .white
        }
    }
    
    open class var grey60: UIColor {
        get {
            return UIColor(named: "Grey60") ?? .white
        }
    }
    
}
